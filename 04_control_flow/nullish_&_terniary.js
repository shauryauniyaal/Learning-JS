// Nullish Coalescing Operator (??) : Used with null and undefined

let val1;

// val1 = null ?? 14
val1 = undefined ?? 14

console.log(val1);


// Terniary Operator (?) : Kind of a shortcut for if else

// condition ? true : false

let value = 40

value > 40 ? console.log("Value greater than 40") : console.log("Value less than or equal to 40");

